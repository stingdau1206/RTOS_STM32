void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(huart->Instance == huart2.Instance)
  {
    HAL_UART_Receive_IT(&huart2, (uint8_t *)&data, 1);
    switch (data)
    {
    case '1':
      osSemaphoreRelease(CountingSemHandle);
      break;
    case '2':
      osSemaphoreRelease(CountingSemHandle);
      osSemaphoreRelease(CountingSemHandle);
      break;
    case '3':
      osSemaphoreRelease(CountingSemHandle);
      osSemaphoreRelease(CountingSemHandle);
      osSemaphoreRelease(CountingSemHandle);
      break;
    default:
      break;
    }
  }
}
